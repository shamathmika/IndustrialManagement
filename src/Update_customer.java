    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         try
        {

            Class.forName("java.sql.Driver");
            Connection conn=(Connection)DriverManager.getConnection("jdbc:mysql://localhost/project", "root", "table");
            Statement st= conn.createStatement();

            String query= "select * from customer where c_id = " + c_id_text.getText() + ";";
            ResultSet rs = st.executeQuery(query);

            while(rs.next())                                                                         //going to each row
            {

                String d2=rs.getString("c_name");
                name_text.setText(d2);
                String d3=rs.getString("phone");
                phone_text.setText(d3);
                String d4=rs.getString("email_id");
                email_id_text.setText(d4);
                String d5=rs.getString("address");
                addr_text.setText(d5);

            }

            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }

        
    }//GEN-LAST:event_jButton2ActionPerformed

