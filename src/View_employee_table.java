    {
        try
        {
            DefaultTableModel model=(DefaultTableModel) id_view.getModel();
            model.setRowCount(0);
            Class.forName("java.sql.Driver");
            Connection conn=(Connection)DriverManager.getConnection("jdbc:mysql://localhost/project", "root", "table");
            Statement st= conn.createStatement();
            
            String query= "select * from employee, factory where employee.f_id = factory.f_id;";
            ResultSet rs = st.executeQuery(query);
            
            while(rs.next())                                                                         //going to each row
            {
                int d1=rs.getInt("e_id");
                String d2=rs.getString("e_name");
                int d3 = rs.getInt("f_id");
                String d5=rs.getString("phone");
                float d9=rs.getFloat("salary");
                String d7=rs.getString("address");
                String d6=rs.getString("email_id");
                String d8=rs.getString("post");
                String d4=rs.getString("f_name");
                String d10=rs.getDate("join_date").toString();
                String d11=rs.getDate("birth_date").toString();
                
                model.addRow(new Object[]{d1, d2, d3, d4, d5, d6, d7, d8, d9, d10, d11});
            }
            
            st.close();
            conn.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }
